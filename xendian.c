/*
	Swap functions to pack/unpack endianess 
*/

	#include <stdint.h>

//********************************************************************************************************
// Configurable defines
//********************************************************************************************************

//********************************************************************************************************
// Local defines
//********************************************************************************************************

	union pack16_union
	{
		int8_t		bytes[2];
		int16_t		word16;
	};

	union pack32_union
	{
		int8_t		bytes[4];
		int32_t		word32;
	};

	union pack64_union
	{
		int8_t		bytes[8];
		int64_t		word64;
	};

//********************************************************************************************************
// Public variables
//********************************************************************************************************

//********************************************************************************************************
// Private variables
//********************************************************************************************************
	
//********************************************************************************************************
// Private prototypes
//********************************************************************************************************

//********************************************************************************************************
// Public functions
//********************************************************************************************************

int64_t xendian_swap64(int64_t x)
{
	union pack64_union in, out;

	in.word64 = x;
	out.bytes[0] = in.bytes[7];
	out.bytes[1] = in.bytes[6];
	out.bytes[2] = in.bytes[5];
	out.bytes[3] = in.bytes[4];
	out.bytes[4] = in.bytes[3];
	out.bytes[5] = in.bytes[2];
	out.bytes[6] = in.bytes[1];
	out.bytes[7] = in.bytes[0];
	return out.word64;
}

int32_t xendian_swap32(int32_t x)
{
	union pack32_union in, out;

	in.word32 = x;
	out.bytes[0] = in.bytes[3];
	out.bytes[1] = in.bytes[2];
	out.bytes[2] = in.bytes[1];
	out.bytes[3] = in.bytes[0];
	return out.word32;
}

int16_t xendian_swap16(int16_t x)
{
	union pack16_union in, out;

	in.word16 = x;
	out.bytes[0] = in.bytes[1];
	out.bytes[1] = in.bytes[0];
	return out.word16;
}


